# ЕГЭ Задача 5: Системы счисления
# Преобразование чисел между разными системами счисления

def task5_solution():
    """
    Задача: Перевести число из десятичной системы в двоичную
    """
    n = int(input("Введите число в десятичной системе: "))
    binary = bin(n)[2:]  # [2:] убирает префикс '0b'
    return binary

if __name__ == "__main__":
    result = task5_solution()
    print(f"Число в двоичной системе: {result}")
