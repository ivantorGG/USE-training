# Решение простых логических задач

def task1_solution():
    """
    Задача: Найти минимальное число, которое нужно вычесть из суммы,
    чтобы получить число, делящееся на 7
    """
    n = int(input("Введите сумму: "))
    remainder = n % 7
    return remainder

if __name__ == "__main__":
    result = task1_solution()
    print(f"Ответ: {result}")
