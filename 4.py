# ЕГЭ Задача 4: Числовые последовательности
# Работа с арифметическими прогрессиями

def task4_solution():
    """
    Задача: Найти n-ый член арифметической прогрессии
    """
    a1 = int(input("Введите первый член (a1): "))
    d = int(input("Введите разность (d): "))
    n = int(input("Введите номер члена (n): "))
    
    an = a1 + (n - 1) * d
    return an

if __name__ == "__main__":
    result = task4_solution()
    print(f"n-ый член прогрессии: {result}")
